/**
 * Bir İngilizce kelimenin Türkçe çevirisini döndürür.
 * Bulunamazsa null döner.
 * @param {string} word
 * @returns {string|null}
 */
export function getTurkishTranslation(word) {
  if (!word) return null;
  const key = String(word).trim().toLowerCase();
  return TR[key] || null;
}

